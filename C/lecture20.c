// 문제5. 구구단 중 n단~m단의 홀수단만 1~limit까지 곱 중에서 짝수값만 출력
// n, m, limit = 지정된 값
/////////////////////////////////////////////////////////
int main(void){
    int n = 4;
    int m = 19;
    int limit = 14;
    for(int dan=n; dan<=m; dan++){
        if(dan%2 != 0){
            printf("====== %d단 ======\n", dan);
            for(int i=1; i<=limit; i++){
                if((dan * i) %  2 == 0){
                    printf("%d X %d = %d\n", dan, i, dan*i);
                }
            }
        }
    }
}
